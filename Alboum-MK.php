        const btn = document.querySelector(`.wave-play-btn[data-wave="${track.id}"]`);
        btn.addEventListener('click', () => {
          if (waves[track.id].isPlaying()) {
            waves[track.id].pause();
            btn.textContent = '▶️';
          } else {
            Object.keys(waves).forEach(id => {
              if (id != track.id) {
                waves[id].pause();
                document.querySelector(`.wave-play-btn[data-wave="${id}"]`).textContent = '▶️';
              }
            });
            waves[track.id].play();
            btn.textContent = '⏸️';
          }
        });
      });
    });
