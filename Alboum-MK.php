function formatTime(seconds) {
  const minutes = Math.floor(seconds / 60) || 0;
  const secs = Math.floor(seconds % 60) || 0;
  return `${String(minutes).padStart(2, "0")}:${String(secs).padStart(2, "0")}`;
}



